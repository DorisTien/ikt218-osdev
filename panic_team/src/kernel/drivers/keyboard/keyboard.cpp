keyboard_callback global_cb = nullptr;
void *global_ctx = nullptr;

void hook_keyboard(keyboard_callback cb, void *ctx)
{
    global_cb = cb;
    global_ctx = ctx;
}

void handle_keyboard_interrupt()
{
    uint8_t scancode = inb(0x60);
    if (scancode > SC_MAX)
        return; // Over SC_MAX is Release (I think)

    if (global_cb != nullptr)
    {
        global_cb(scancode, global_ctx);
    }
}

char scancode_to_ascii(uint8_t scancode)
{
    return sc_ascii[(int)scancode];
}
